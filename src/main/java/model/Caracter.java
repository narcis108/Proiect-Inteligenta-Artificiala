package main.java.model;

public class Caracter {
    private String nume;
    private String sat;
    private String rank;
    private String clan;
    private String bestie;
    private String tipOchi;
    private String sex;
    private String primaAparitie;
    private String elementDominant;
    private String abilitateInascuta;
    private String traieste;

    public String getNume() {
        return nume;
    }

    public void setNume(String nume) {
        this.nume = nume;
    }

    public String getSat() {
        return sat;
    }

    public void setSat(String sat) {
        this.sat = sat;
    }

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }

    public String getClan() {
        return clan;
    }

    public void setClan(String clan) {
        this.clan = clan;
    }

    public String getBestie() {
        return bestie;
    }

    public void setBestie(String bestie) {
        this.bestie = bestie;
    }

    public String getTipOchi() {
        return tipOchi;
    }

    public void setTipOchi(String tipOchi) {
        this.tipOchi = tipOchi;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getPrimaAparitie() {
        return primaAparitie;
    }

    public void setPrimaAparitie(String primaAparitie) {
        this.primaAparitie = primaAparitie;
    }

    public String getElementDominant() {
        return elementDominant;
    }

    public void setElementDominant(String elementDominant) {
        this.elementDominant = elementDominant;
    }

    public String getAbilitateInascuta() {
        return abilitateInascuta;
    }

    public void setAbilitateInascuta(String abilitateInascuta) {
        this.abilitateInascuta = abilitateInascuta;
    }

    public String getTraieste() {
        return traieste;
    }

    public void setTraieste(String traieste) {
        this.traieste = traieste;
    }
}
